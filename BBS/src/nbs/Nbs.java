package nbs;

public class Nbs {
	private int nbsID;
	private String nbsTitle;
	private String userID;
	private String nbsDate;
	private String nbsContent;
	private String nbsImage;
	private int nbsHit;
	public int getNbsID() {
		return nbsID;
	}
	public void setNbsID(int nbsID) {
		this.nbsID = nbsID;
	}
	public String getNbsTitle() {
		return nbsTitle;
	}
	public void setNbsTitle(String nbsTitle) {
		this.nbsTitle = nbsTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getNbsDate() {
		return nbsDate;
	}
	public void setNbsDate(String nbsDate) {
		this.nbsDate = nbsDate;
	}
	public String getNbsContent() {
		return nbsContent;
	}
	public void setNbsContent(String nbsContent) {
		this.nbsContent = nbsContent;
	}
	public String getNbsImage() {
		return nbsImage;
	}
	public void setNbsImage(String nbsImage) {
		this.nbsImage = nbsImage;
	}
	public int getNbsHit() {
		return nbsHit;
	}
	public void setNbsHit(int nbsHit) {
		this.nbsHit = nbsHit;
	}
}
